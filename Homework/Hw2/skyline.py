def drawStreet():
    t.pencolor("white")
    t.penup()
    t.rt(90)
    t.fd(75)
    t.lt(90)
    t.bk(400)
    t.pendown()
    t.fd(75)
    t.penup()
    t.setpos(295.00,-100.00)
    t.pendown()
    t.fd(200)
    t.penup()
    t.setpos(0.00,-100.00)
    t.rt(90)
    t.fd(50)
    t.lt(90)
    t.pendown()
    t.fd(400)
    t.bk(800)
    t.fd(75)
